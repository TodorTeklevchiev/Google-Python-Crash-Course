"""
The comparison operators include:

== - Equality
!= - Not equal to
> - Greater than
< - Less than
>= - Greater than or equal to
<= - Less than or equal to

"""

# PART 1: Equality == and Not Equal To != Operators

# ========================================================================
# To check if two values are the same, use the equality operator - ==
# Examples:
print(32 == 30 + 2) # The == operator checks if 2 values are equal
                    # to each other. If they are equal,
                    # Python returns a True result.

print(5+10 == 6+7) # If two values are not equal,
                    # Python returns a False result.

# =========================================================================
# To check if two values are not the same, use the not equal to operator - !=
# Examples:
print(10-4 != 10+4) # The != operator checks if 2 values are NOT equal to each other
                    # If true, Python returns True result.

print(9/3 != 3*1) # In this example the result is False. So Pyton returns False value.


# =========================================================================
# The equality operator ( == ) versus the equals operator( = )

# The = equals assigment operator is used to assign a value to a variable.
my_variable = 3*5   # Assigns a value to my_variable    
print(my_variable)  # Printing the variable returns the value assigned to the variable.

# The == equality operator checks if the values of two expressions on either side of the 
# == operator are equivalent to one another.
print(my_variable == 3*5) # Printing the variable returns a Boolean
                            # True or False result

# PART 2: Greater Than > and Less Than < Operators


# ===========================================================================
# To check if one value is larger than another value, use the greater operator - >
# Example:
print(11 > 3*3) # The > operator checks if the left value is greater than the right one.
                # If true, Python returns a True result.

print(4/2 > 8-4)# The > operator finds that the left value is NOT greater than the right value
                # in this case Python will return False result.

# To check if one value is smallet than another value, use the less than operator - <
# Example:
print(4/2 < 8-4) # The < operator checks if the left value is less than the right value,
                # if true, Python returns True result.

print(11 < 3*3) # The < operator checks if the left value is less than the right value,
                # if false, Python returns False result.














